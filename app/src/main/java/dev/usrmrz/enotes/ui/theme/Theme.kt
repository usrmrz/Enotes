@Composable
fun EnotesTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val enotesColorScheme = if (darkTheme) darkColorScheme else lightColorScheme

    MaterialTheme(
        colorScheme = enotesColorScheme,
        typography = Typography,
        shapes = Shapes,
        content = content
    )
}